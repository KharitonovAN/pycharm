
def sum(answer):
    """Функция складывает двух чисел"""
    number_1 = int(input())
    number_2 = int(input())
    answer = number_1 + number_2
    return answer


def subtraction(answer):
    """Функция вычитания двух чисел"""
    number_1 = int(input())
    number_2 = int(input())
    answer = number_1 - number_2
    return answer
